// ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ
router.post("/:id/recibir-completo", authenticate, async (req, res) => {
  try {
    const { id } = req.params;

    const pedido = await prisma.purchaseOrder.findUnique({
      where: { id },
      include: { lineas: true }
    });

    if (!pedido) {
      return res.status(404).json({ error: "Pedido no encontrado" });
    }

    // Actualizar todas las l√≠neas como recibidas
    await Promise.all(
      pedido.lineas.map(linea =>
        prisma.purchaseOrderLine.update({
          where: { id: linea.id },
          data: { cantidad_recibida: linea.cantidad }
        })
      )
    );

    // Actualizar el pedido
    const pedidoActualizado = await prisma.purchaseOrder.update({
      where: { id },
      data: {
        estado: 'recibido',
        fecha_recibido: new Date(),
        fecha_ultima_recepcion: new Date()
      },
      include: { lineas: true }
    });

    res.json(pedidoActualizado);
  } catch (error) {
    console.error("Error recibiendo mercanc√≠a:", error);
    res.status(500).json({ error: "Error al recibir mercanc√≠a" });
  }
});

